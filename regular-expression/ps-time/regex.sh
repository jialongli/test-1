#!/bin/bash


# 匹配 00:00 类字符串
echo ">> 匹配 00:00 类字符串"
cat time.txt | egrep "^(([0-9]|[0-9]\\w+)\\:([0-9]|[0-9]\\w+)){1,1}$"

# 匹配 00:00:00 类字符串
echo ">> 匹配 00:00:00 类字符串"
cat time.txt | egrep "^(([0-9]|[0-9]\\w+)\\:){2,2}([0-9]|[0-9]\\w+)$"

# 匹配 00-00:00:00 类字符串
echo ">> 匹配 00-00:00:00 类字符串"
cat time.txt | egrep "^([0-9]|[0-9]\\w+)\\-(([0-9]|[0-9]\\w+)\\:){2,2}[0-9]\\w+$"
